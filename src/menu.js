function getLorem(count, type) {
   var result = "";

   switch (type) {
      case "p":
         for (var i = 0; i < count; i++) {
            result += loremIpsum[Math.floor( Math.random() * loremIpsum.length )] + "\n\n";
         };
         break;
      case "w":
         var cnt = 0;

         while(cnt < count) {
            var sampleStringWords = loremIpsum[Math.floor( Math.random() * loremIpsum.length )].split(" ");
            for (var i = 0; i < sampleStringWords.length; i++) {
               
               if(cnt >= count)
                  break;

               result += sampleStringWords[i] + " ";
               cnt++;
            };
         }

         break;
   }

   return result;
}

